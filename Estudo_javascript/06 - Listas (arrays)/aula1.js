
/*
comandos listas
pop = retira o ultimo da lista
push = insere no final da lista
shift = retira o primeiro da lista
*/

const notas = [];
const lista = [1,2,3,4];
const nomes = ['Diogo', 'Diana', 'Julia'];

notas.push(10);
console.log(notas);

lista.pop();
console.log(lista);

nomes.shift();
console.log(nomes);